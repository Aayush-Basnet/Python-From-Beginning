
letter = input("Enter letter: ")

lowercase= int
uppercase = int
lowercase=(letter=='a'or letter=='i'or letter=='o'or letter=='u'or letter=='e')
uppercase=(letter=='A'or letter=='E'or letter=='I'or letter=='O'or letter=='U')

if(lowercase or uppercase):
    print(f"{letter} is vowel")
else:
    print(f"{letter} is not vowel")